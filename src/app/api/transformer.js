import * as Babel from "@babel/standalone/babel.js";
////////////////////////////////////////////////////////////////////////////////////////////////////

export const transform = code => {
    try {
        const tCode = Babel.transform(code, {
            presets: [ "es2015", "react", "stage-0" ]
        }).code;
        return tCode;
    } catch (e) {
        return e.message;
    }
};
